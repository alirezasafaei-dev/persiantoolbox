function withSeoContent(entry: RawToolEntry): ToolEntry {
  const tier = resolveToolTier(entry);
  const base: ToolEntry = { ...entry, tier };
  if (entry.kind !== 'tool') {
    return base;
  }
  const introLen = entry.content?.intro?.trim().length ?? 0;
  const faqLen = entry.content?.faq?.length ?? 0;
  const stepsLen = entry.content?.steps?.length ?? 0;
  const hasStrongContent = introLen >= 40 && faqLen >= 2 && stepsLen > 0;
  if (hasStrongContent) {
    return base;
  }
  const generated = buildDefaultToolContent(base);
  const content: ToolContent = {
    intro: introLen >= 40 && entry.content?.intro ? entry.content.intro : generated.intro,
  };
  const sections = entry.content?.sections ?? generated.sections;
  if (sections) {
    content.sections = sections;
  }
  const steps =
    stepsLen > 0 && entry.content?.steps ? entry.content.steps : (generated.steps ?? []);
  content.steps = steps;
  const tips =
    entry.content?.tips && entry.content.tips.length > 0
      ? entry.content.tips
      : (generated.tips ?? []);
  if (tips.length > 0) {
    content.tips = tips;
  }
  content.faq = faqLen >= 2 && entry.content?.faq ? entry.content.faq : (generated.faq ?? []);
  return { ...base, content };
}

